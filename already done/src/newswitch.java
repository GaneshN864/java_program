public class newswitch {
    public static void main(String[] args)
    {
        int a=10;
        switch (a)
        {
            case 10->
            {
                System.out.print("hi");
                System.out.print("hello");
            }
            case 11->
            {
                System.out.print("hi1");
                System.out.print("hello1");
            }
        }
    }
}